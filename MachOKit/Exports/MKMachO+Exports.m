//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//
#pragma mark - 	Exports Information
//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//

//|++++++++++++++++++++++++++++++++++++|//
- (MKOptional*)exportsInfo
{
    if (_exportsInfo == nil)
    {
        NSError *exportsInfoError = nil;
        
        MKExportsInfo *exportsInfo = [[MKExportsInfo alloc] initWithParent:self error:&exportsInfoError];
		if (exportsInfo)
			_exportsInfo = [[MKOptional alloc] initWithValue:exportsInfo];
		else if (exportsInfoError /* Only failed if we have an error */)
			_exportsInfo = [[MKOptional alloc] initWithError:exportsInfoError];
		else
			_exportsInfo = [MKOptional new];
		
		[exportsInfo release];
    }
    
    return _exportsInfo;
}

//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//
#pragma mark -  MKNode
//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//

//|++++++++++++++++++++++++++++++++++++|//
+ (MKNodeFieldBuilder*)_exportsInfoFieldBuilder
{
    MKNodeFieldBuilder *exportsInfo = [MKNodeFieldBuilder
        builderWithProperty:MK_PROPERTY(exportsInfo)
        type:[MKNodeFieldTypeNode typeWithNodeType:MKExportsInfo.class]
    ];
    exportsInfo.description = @"Export Info";
    exportsInfo.options = MKNodeFieldOptionDisplayAsChild;
    
    return exportsInfo;
}

@end
